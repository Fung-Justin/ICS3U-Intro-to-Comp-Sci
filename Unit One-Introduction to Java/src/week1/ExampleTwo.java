package week1;

public class ExampleTwo {
    public static void main(String[] args) {

        // Letter "J"
        System.out.println(" \t JJJJJJJJJJ");
        System.out.println("\t     J \n\t     J");
        System.out.println("\tJ    J");
        System.out.println(" \t  JJJ");
        
        // Letter "A"
        System.out.println("\n\t      A");
        System.out.println("\t    A   A");
        System.out.println("\t   A     A");
        System.out.println("\t  AAAAAAAAA");
        System.out.println("\t A         A \n\t A         A  ");

        // Letter "C"
        System.out.println(" \t   cc");
        System.out.println(" \t  c   c");
        System.out.println(" \t c \n \t c");
        System.out.println(" \t  c   c");
        System.out.println(" \t   cc");

    }
}
